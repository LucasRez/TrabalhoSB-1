SWAP: MACRO &A, &B, &t
	COPY &A, &T
	COPY &B, &A
	COPY &T, &B
	END MACRO


SECTION DATA
	B:		SPACE
	H:		SPACE
	R:		SPACE
	DOIS:	CONST		2
	TRIANGULO EQU 1
	TESTAMACRO EQU 1
SECTION TEXT
		INPUT		B
		INPUT		H
		LOAD		B
		MULT		H
		IF TESTAMACRO
		SWAP TESTAMACRO, TRIANGULO, B
		IF TRIANGULO
		DIV		DOIS
		STORE		R
		OUTPUT	R
		STOP

